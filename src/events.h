#define E_OTHER 0xffff
#define E_OK 0x1100
#define E_CANCEL 0x1101
#define E_EXIT 0x1102
#define E_EXIT_AND_SAVE 0x1103
#define E_NOWAIT 0x1104
#define E_CLOSE 0x000b				/* C-K close window         */
#define E_CHANGE_SCREEN 0xff01
#define E_NEXT_WINDOW 0x09			/* Ctrl-I (TAB) next window */
#define E_PREV_WINDOW KEY_BTAB
#define E_NEXT_WINDOW2 0x09			/* Ctrl-I (TAB) next window */
#define E_PREV_WINDOW2 0x1a			/* Ctrl-Z prev window */
#define E_NEXT 0x0009
#define E_PREV 0x001a
#define E_COMPONENT_ID 0xfffa
